CREATE TABLE PERSINFO (
	NAME VARCHAR2(20),
	AADHARNO VARCHAR2(14),
	PANCARDNO VARCHAR2(14)
);
CREATE OR REPLACE TRIGGER BIT1
BEFORE
INSERT
ON PERSINFO
REFERENCES NEW AS N
FOR EACH ROW
BEGIN
	:N.AADHARNO := TRANSLATE(UPPER(:N.AADHARNO),'1QWERTYUIOPASDFGHJKLZXCVBNM!@#$%^&*()-_=+[]{};:,.<>?/','1');
	IF LENGTH(:N.AADHARNO) = 12 THEN
		:N.AADHARNO := SUBSTR(:N.AADHARNO,1,4) || '-' || SUBSTR(:N.AADHARNO,5,4) || '-' || SUBSTR(:N.AADHARNO,9,4);
	ELSE
		RAISE_APPLICATION_ERROR(-20001,'INCORRECT AADHAR NUMBER');
	END IF;			
END;
