<<OUTER>>  -- BLOCK LABEL
DECLARE
   X NUMBER(3) := 10;
BEGIN
   DBMS_OUTPUT.PUT_LINE('OUTER BLOCK X FROM WITHIN OUTER BLOCK = ' || X);  -- 10
   DBMS_OUTPUT.PUT_LINE(RPAD('=',80,'='));
   <<MIDDLE>>
   DECLARE
      X NUMBER(3) := 20;
   BEGIN
      DBMS_OUTPUT.PUT_LINE('X = ' || X);  -- 20
      <<INNER>> DECLARE
		X NUMBER(3) := 30;   
      BEGIN
		 DBMS_OUTPUT.PUT_LINE(RPAD('=',80,'='));
         DBMS_OUTPUT.PUT_LINE('INNER BLOCK X FROM WITHIN INNER BLOCK = ' || X);  -- 30
         DBMS_OUTPUT.PUT_LINE('MIDDLE BLOCK X FROM WITHIN INNER BLOCK = ' || MIDDLE.X);  -- 20
         DBMS_OUTPUT.PUT_LINE('OUTER BLOCK X FROM WITHIN INNER BLOCK = ' || OUTER.X);  -- 10
         DBMS_OUTPUT.PUT_LINE(RPAD('=',80,'='));
      END INNER; 
	  -- WHICH X ARE REFERRABLE HERE? MIDDLE.X , OUTER.X
      DBMS_OUTPUT.PUT_LINE('MIDDLE BLOCK X FROM WITHIN MIDDLE BLOCK = ' || /*MIDDLE.*/X);  -- 20
--	  DBMS_OUTPUT.PUT_LINE('OUTER BLOCK X FROM WITHIN OUTER BLOCK = ' || INNER.X);  -- ERROR
      DBMS_OUTPUT.PUT_LINE('OUTER BLOCK X FROM WITHIN MIDDLE BLOCK = ' || OUTER.X);  -- 10
   END OUTER;
-- WHICH X ARE REFERRABLE HERE? OUTER.X
   DBMS_OUTPUT.PUT_LINE('OUTER BLOCK X FROM WITHIN OUTER BLOCK = ' || X);  -- 10
END GANESH;
