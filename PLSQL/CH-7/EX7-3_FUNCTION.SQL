CREATE OR REPLACE FUNCTION GET_DEPT_TOTAL_SAL
(DEPT_NAME IN DEPARTMENTS.DEPARTMENT_NAME%TYPE)
RETURN NUMBER
AS
	V_SAL NUMBER := 0;
BEGIN
	SELECT NVL(SUM(SALARY),0) INTO V_SAL FROM EMPLOYEES WHERE DEPARTMENT_ID IN (SELECT DEPARTMENT_ID FROM DEPARTMENTS WHERE DEPARTMENT_NAME = DEPT_NAME);
	RETURN V_SAL;
EXCEPTION
	WHEN NO_DATA_FOUND THEN
		DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
		RETURN NULL;
	WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('OTHERS');
		RETURN NULL;
END GET_DEPT_TOTAL_SAL;
