CREATE OR REPLACE FUNCTION GET_REGION_INFO
(C_ID IN COUNTRIES.COUNTRY_ID%TYPE, C_NAME OUT COUNTRIES.COUNTRY_NAME%TYPE)
RETURN R1%ROWTYPE
AS
	V_R1 R1%ROWTYPE;
BEGIN
	SELECT COUNTRY_NAME INTO C_NAME FROM COUNTRIES WHERE COUNTRY_ID = C_ID;
	SELECT REGION_ID,REGION_NAME INTO V_R1.REGION_ID , V_R1.REGION_NAME FROM REGIONS WHERE REGION_ID = (SELECT REGION_ID FROM COUNTRIES WHERE COUNTRY_ID = C_ID);
RETURN V_R1;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        DBMS_OUTPUT.PUT_LINE('NO DATA FOUND');
        RETURN NULL;
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('OTHERS');
        RETURN NULL;
END GET_REGION_INFO;
/
