/* STORED PROCEDURE */ 
CREATE OR REPLACE PROCEDURE GET_Y
(X NUMBER, Y IN OUT NUMBER, Z IN OUT NUMBER)
IS
BEGIN
	Y := X / 3 ;  -- REPLACE 0 WITH 4 AND SEE THE RESULT.
	GET_Z(X , Y , Z);
	DBMS_OUTPUT.PUT_LINE('Y = ' || Y);
	DBMS_OUTPUT.PUT_LINE('Z = ' || Z);
-- MAKE EXCEPTION PART REMARK AND SEE THE RESULT.
 EXCEPTION
	WHEN ZERO_DIVIDE THEN  
		RAISE_APPLICATION_ERROR(-20000,'YOU TRIED TO DIVIDE X WITH ZERO IN GET_Y PROCEDURE.');  
	WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('EXCEPTION IS HANDELED BY OTHERS OF GET_Y');  
END;
