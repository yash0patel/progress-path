BEGIN
	DBMS_OUTPUT.PUT_LINE('OUTER MOST BLOCK STARTS HERE.');
	DECLARE
		X NUMBER(5) := 300;
		Y NUMBER(5);
		Z NUMBER(5);
	BEGIN
	/* EXECUTE THIS BLOCK THREE TIMES. ONE FOR HANDLED EXCEPTIONS.
	SECOND TIME MAKE EXCEPTION PART REMARK IN GET_Z PROCEDURE AND RECREATING THE PROCEDURE. 
	AND THIRD TIME MAKE EXCEPTION PART REMARK IN GET_Y PROCEDURE AND RECREATING THE PROCEDURE.*/
		GET_Y(X, Y ,Z);
		DBMS_OUTPUT.PUT_LINE('X = ' ||  X);
	EXCEPTION
		WHEN ZERO_DIVIDE THEN
			DBMS_OUTPUT.PUT_LINE('CALLING BLOCK IS HANDLING EXCEPTION.');    
			RAISE;  
		WHEN OTHERS THEN
			DBMS_OUTPUT.PUT_LINE('OTHERS OF INNER BLOCK EXECUTED.');
--			RAISE_APPLICATION_ERROR(-20777,'EXCEPTION IS HANDLED BY OTHERS OF CALLING ANONYMOUS BLOCK');     
	END;
	DBMS_OUTPUT.PUT_LINE('OUTER MOST BLOCK ENDS HERE.');
EXCEPTION
	WHEN ZERO_DIVIDE THEN
			DBMS_OUTPUT.PUT_LINE('OUTER MOST BLOCK ZERO_DIVIDE.');      
	WHEN OTHERS THEN
		DBMS_OUTPUT.PUT_LINE('CALLING PROGRAM OUTER MOST BLOCK WHEN OTHERS');   
END;		
