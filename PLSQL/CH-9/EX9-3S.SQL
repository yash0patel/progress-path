CREATE OR REPLACE PACKAGE ADD_PACKAGE
IS
	FUNCTION ADD(V1 VARCHAR2,V2 VARCHAR2) RETURN VARCHAR2;
	FUNCTION ADD(V1 NUMBER,V2 NUMBER) RETURN NUMBER;
	FUNCTION ADD(V1 DATE,V2 NUMBER) RETURN DATE;
	FUNCTION ADD(V1 NUMBER,V2 DATE) RETURN DATE;
END	ADD_PACKAGE;
/


/*
WRITE A PACKAGE WHICH WILL CONTAIN OVERLOADED FUNCTIONS NAMED 'ADD'. 

1. IF WE SUPPLY BOTH CHARACTER VALUES, IT SHOULD CONCAT AND RETURN IT WITH SPACE IN BETWEEN.

2. IF WE SUPPLY BOTH NUMBER VALUES, IT SHOULD ADD IT AND RETURN SUM OF THEM.

3. IF WE SUPPLY DATE AND NUMBER, IT SHOULD ADD/SUBTRACT NUMBER OF DAYS TO THE DATE AND RETURN DATE.

4. IF WE SUPPLY NUMBER AND DATE, IT SHOULD ADD/SUBTRACT NUMBER OF DAYS TO THE DATE AND RETURN DATE.
*/