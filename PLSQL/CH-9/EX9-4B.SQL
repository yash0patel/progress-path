CREATE OR REPLACE PACKAGE BODY INTEREST_CALC_PACKAGE
IS
	FUNCTION INTEREST_CALC(P_AMT PLS_INTEGER,ROI PLS_INTEGER,PERIOD PLS_INTEGER) RETURN PLS_INTEGER
	IS
		SI PLS_INTEGER;
	BEGIN
		SI := P_AMT * (ROI/100) * PERIOD;
		RETURN SI;
	END INTEREST_CALC;
	
	FUNCTION INTEREST_CALC(P_AMT NUMBER,ROI NUMBER,PERIOD NUMBER) RETURN NUMBER
	IS
		CI NUMBER;
	BEGIN
		CI := P_AMT * POWER(1 + (ROI/100), PERIOD) - P_AMT;
		RETURN CI;
	END INTEREST_CALC;
END INTEREST_CALC_PACKAGE;
